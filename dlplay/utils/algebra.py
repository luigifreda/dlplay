import torch


def gram_matrix(y):
    (b, ch, h, w) = y.size()
    features = y.view(b, ch, w * h)
    features_t = features.transpose(1, 2)  # transpose the last two dimensions
    gram = features.bmm(features_t) / (ch * h * w)
    return gram


def gram_matrix_einops(y):
    b, ch, h, w = y.shape
    return torch.einsum("bchw,bdhw->bcd", [y, y]) / (h * w)
